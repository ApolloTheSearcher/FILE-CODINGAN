let a =  true;
let b = false;

console.log(typeof a); // MENGETAHUI TIPE DATA

// Selain itu juga bisa menggunakan comparator dan operato logika
console.log(a == b); // == untuk mengecek apakah nilai a dan b sama atau tidak
console.log(a != b); // != untuk mengecek apakah nilai a dan b tidak sama atau tidak
console.log(a > b); // > untuk mengecek apakah nilai a lebih besar dari b
console.log(a < b); // < untuk mengecek apakah nilai a lebih kecil dari b
console.log(a >= b); // >= untuk mengecek apakah nilai a lebih besar atau sama dengan b
console.log(a <= b); // <= untuk mengecek apakah nilai a lebih kecil atau sama dengan b
// diatas comparator

// Operasi logika
console.log(a && b); // && untuk mengecek apakah nilai a dan b sama atau tidak
console.log(a || b); // || untuk mengecek apakah nilai a dan b tidak sama atau tidak
console.log(!a); // ! untuk mengecek apakah nilai a tidak sama atau tidak
