// Terus untuk operasi aritmatika di javascript sama seperti pada bahasa lainnya
let a = 100;
let b = 50;

console.log(a + b); // untuk menjumlahkan
console.log(a - b); // untuk mengurangkan
console.log(a * b); // untuk mengalikan
console.log(a / b); // untuk membagi
console.log(a % b); // untuk menghitung sisa bagi

// Increment dan decrement 
// Increment (++)
Increment = b;
Increment++;
console.log(Increment); // disini untuk menambahkan 1 ke variabel Increment

// decrement (--)
Decrement = b;
Decrement--;
console.log(Decrement); // disini untuk mengurangkan 1 ke variabel Decrement

