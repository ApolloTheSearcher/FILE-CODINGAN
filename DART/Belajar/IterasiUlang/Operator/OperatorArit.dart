void main() {
  // Pada operator aritmatika pada dart hampir sama saja dengan bahasa lainnya
  // tetapi untuk menggunakan cara pembagiannya terbagi menjadi 2 yaitu pembagian yang hasilnya double atau int.
  // Untuk pembagian yang hasilnya double kita dapat menggunakan operator ( / )
  // Sedangkan untuk pembagian yang hasilnya int kita dapat menggunakan operator ( ~/ )
  var a = 10;
  var b = 7;
  var pembagianDouble = a / b;
  print(pembagianDouble);
  var pembagianInt = a ~/ b;
  print(pembagianInt);
}
