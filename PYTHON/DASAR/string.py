#ESCAPE CHARACTERS (perintah yang menggunakan \ <= escape character)
# \n new line (untuk new line digunakannya didalam string)
# \\ blackslash (untuk menggunakan \ didalam string)
# \' single quote (untuk menambahkan tanda petik didalam string)
# \r carriage return (fungsi sama dengan \n hanya saja \r ini formatnya return bukan garis baru didalam string)
# \t tab (digunakan untuk memberi jarak tab didalam string)
# \b backspace (digunakan untuk menghapus kata di dalam string)
# \f form feed 
# \ooo octal value (digunakan untuk mengubah nilai dalam bentuk octal)
# \xhh hexadecimal value (digunakan untuk mengubah nilai dalam bentuk hexadecimal)

