'''
Didalam suatu program ada yang namanya itu lambda function.
lambda fungsi atau biasanya disebut dengan (fungsi anonim) digunakan untuk persingkat / singkatan satu baris untuk fungsi
'''
# Contoh 1
def add(x, y):
    return x + y
# kalau kita ubah menjadi lambda
add = lambda x, y: x + y
# Jadi untuk lambda penulisannya itu ( nama fungsi = lambda parameter : return/prosesnya )

